# 2)написати прогу яка вибирає зі введеної строки числа і виводить їх
# так як вони написані
# наприклад:
sti = 'as 23 fdfdg544 34'
# # # #   23, 544, 34              #вивело в консолі
new_st = ''
for i in sti:
    if i.isdigit():
        new_st += i
    else:
        new_st += ' '
new_st = ', '.join(new_st.split())
print(new_st)
